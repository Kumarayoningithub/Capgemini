package program_variable.homeworktypecast;

public class _longtypecast {

	public static void main(String[] args) {
		
		long a = 70;
		
		    float e = a; // size --> 4 byte 
	        System.out.println(e); // long to float 
	        
	        double f = a; // size --> 8 byte 
	        System.out.println(f); // long to double 
	        
	        char ch = (char)a; // size-->2 byte
	        System.out.println(ch); // long to char
	        
	        
	        
	        
	        byte b = (byte)a;
	        System.out.println(b);

	        short c = (short)a;
	        System.out.println(c);

	        int d = (int)a;
	        System.out.println(d);

	        
	        
	 
	        
	        

	}

}
