package operators.conditionaloperators;

public class Program1 {

	public static void main(String[] args) {
		// Operand 1 ? operand2 : Operand3
	    // condition
		
		System.out.println(true?10:20);
		System.out.println(false? "A": 65);
		System.out.println(10>'A'? true: false);
		System.out.println("hello"!= "Hello"? 'A': 'B');
		
		//System.out.println(40+30? 'A': 'B');// c error
		
//res = 40>30?"A":true;  IF WE PUT INSIDE THE VARIABLE THEN THE OP2 AND OP3 DATA TYPE SHOULD BE SAME 
		
		
		
		
		System.out.println();
		
		

	}

}
